class PredictionResult:
    """
    Data class encapsulating the complete prediction result with metadata.
    
    This class provides a standardized format for all predictions generated by
    the system, including confidence intervals, contributing factors, and
    model performance metrics.
    
    Attributes:
        flow_type: Type of flow being predicted
        horizon: Prediction time horizon
        timestamps: List of prediction timestamps
        values: Predicted values corresponding to timestamps
        lower_bound: Lower bound of prediction interval (uncertainty)
        upper_bound: Upper bound of prediction interval (uncertainty)
        confidence_score: Overall confidence score (0-1)
        model_used: Name of the model that generated the prediction
        features_importance: Dictionary of feature importance scores
        metadata: Additional prediction metadata
        generated_at: Timestamp when prediction was generated
    """
    
    def __init__(
        self,
        flow_type: FlowType,
        horizon: PredictionHorizon,
        timestamps: List[datetime],
        values: List[float],
        lower_bound: Optional[List[float]] = None,
        upper_bound: Optional[List[float]] = None,
        confidence_score: float = 0.0,
        model_used: str = "unknown",
        features_importance: Optional[Dict[str, float]] = None,
        metadata: Optional[Dict[str, Any]] = None
    ):
        self.flow_type = flow_type
        self.horizon = horizon
        self.timestamps = timestamps
        self.values = values
        self.lower_bound = lower_bound or [v * 0.9 for v in values]  # Default 10% lower bound
        self.upper_bound = upper_bound or [v * 1.1 for v in values]  # Default 10% upper bound
        self.confidence_score = confidence_score
        self.model_used = model_used
        self.features_importance = features_importance or {}
        self.metadata = metadata or {}
        self.generated_at = datetime.now()
        
    def to_dict(self) -> Dict[str, Any]:
        """Convert prediction result to dictionary for serialization."""
        return {
            "flow_type": self.flow_type.value,
            "horizon": self.horizon.value,
            "timestamps": [ts.isoformat() for ts in self.timestamps],
            "values": self.values,
            "lower_bound": self.lower_bound,
            "upper_bound": self.upper_bound,
            "confidence_score": self.confidence_score,
            "model_used": self.model_used,
            "features_importance": self.features_importance,
            "metadata": self.metadata,
            "generated_at": self.generated_at.isoformat()
        }
    
    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> 'PredictionResult':
        """Create prediction result from dictionary."""
        return cls(
            flow_type=FlowType(data["flow_type"]),
            horizon=PredictionHorizon(data["horizon"]),
            timestamps=[datetime.fromisoformat(ts) for ts in data["timestamps"]],
            values=data["values"],
            lower_bound=data.get("lower_bound"),
            upper_bound=data.get("upper_bound"),
            confidence_score=data.get("confidence_score", 0.0),
            model_used=data.get("model_used", "unknown"),
            features_importance=data.get("features_importance"),
            metadata=data.get("metadata")
        )


class LSTMPredictor(nn.Module):
    """
    LSTM-based neural network for time series prediction.
    
    This model captures long-term dependencies in sequential patient flow data
    using stacked LSTM layers with dropout for regularization.
    
    Architecture:
        - Input layer: Sequence of historical values
        - LSTM layers: 2 layers with configurable hidden size
        - Dropout: Prevents overfitting
        - Fully connected: Maps LSTM output to predictions
        
    Args:
        input_size: Number of input features (typically 1 for univariate)
        hidden_size: Number of LSTM hidden units
        num_layers: Number of stacked LSTM layers
        output_size: Number of output values to predict
        dropout: Dropout probability for regularization
    """
    
    def __init__(
        self,
        input_size: int = 1,
        hidden_size: int = 64,
        num_layers: int = 2,
        output_size: int = 1,
        dropout: float = 0.2
    ):
        super(LSTMPredictor, self).__init__()
        
        self.hidden_size = hidden_size
        self.num_layers = num_layers
        
        # LSTM layers for sequence modeling
        self.lstm = nn.LSTM(
            input_size=input_size,
            hidden_size=hidden_size,
            num_layers=num_layers,
            batch_first=True,
            dropout=dropout if num_layers > 1 else 0
        )
        
        # Dropout layer for regularization
        self.dropout = nn.Dropout(dropout)
        
        # Fully connected output layer
        self.fc = nn.Linear(hidden_size, output_size)
        
        # Initialize weights using Xavier initialization for better convergence
        self._init_weights()
        
    def _init_weights(self):
        """Initialize model weights using Xavier initialization."""
        for name, param in self.named_parameters():
            if 'weight' in name:
                nn.init.xavier_uniform_(param)
            elif 'bias' in name:
                nn.init.zeros_(param)
    
    def forward(self, x: torch.Tensor) -> torch.Tensor:
        """
        Forward pass through the network.
        
        Args:
            x: Input tensor of shape (batch_size, sequence_length, input_size)
            
        Returns:
            Output tensor of shape (batch_size, output_size)
        """
        # Initialize hidden state with zeros
        h0 = torch.zeros(self.num_layers, x.size(0), self.hidden_size).to(x.device)
        c0 = torch.zeros(self.num_layers, x.size(0), self.hidden_size).to(x.device)
        
        # Forward propagate LSTM
        out, _ = self.lstm(x, (h0, c0))
        
        # Take the output from the last time step
        out = out[:, -1, :]
        
        # Apply dropout
        out = self.dropout(out)
        
        # Pass through fully connected layer
        out = self.fc(out)
        
        return out


class TemporalConvolutionalNetwork(nn.Module):
    """
    Temporal Convolutional Network for time series prediction.
    
    TCNs use dilated causal convolutions to capture long-range patterns
    efficiently with parallel processing capabilities.
    
    Architecture:
        - Multiple convolutional layers with increasing dilation
        - Residual connections for gradient flow
        - Causal padding to maintain temporal order
        
    Args:
        input_size: Number of input channels
        num_channels: List of channel sizes for each layer
        kernel_size: Size of convolutional kernels
        dropout: Dropout probability
    """
    
    def __init__(
        self,
        input_size: int = 1,
        num_channels: List[int] = [32, 64, 64],
        kernel_size: int = 3,
        dropout: float = 0.2
    ):
        super(TemporalConvolutionalNetwork, self).__init__()
        
        layers = []
        num_levels = len(num_channels)
        
        for i in range(num_levels):
            dilation = 2 ** i  # Exponential dilation for larger receptive field
            in_channels = input_size if i == 0 else num_channels[i-1]
            out_channels = num_channels[i]
            
            # Create causal convolutional block
            layers += [
                CausalConv1d(in_channels, out_channels, kernel_size, dilation),
                nn.ReLU(),
                nn.Dropout(dropout)
            ]
        
        self.network = nn.Sequential(*layers)
        self.final_conv = nn.Conv1d(num_channels[-1], 1, kernel_size=1)
        
    def forward(self, x: torch.Tensor) -> torch.Tensor:
        """
        Forward pass through TCN.
        
        Args:
            x: Input tensor of shape (batch_size, channels, sequence_length)
            
        Returns:
            Output tensor of shape (batch_size, 1)
        """
        # Apply temporal convolutional layers
        out = self.network(x)
        
        # Global average pooling across time dimension
        out = out.mean(dim=-1)
        
        # Final convolution to produce prediction
        out = self.final_conv(out.unsqueeze(-1)).squeeze(-1)
        
        return out


class CausalConv1d(nn.Module):
    """
    1D Causal Convolution for temporal models.
    
    Ensures that predictions at time t depend only on inputs up to time t,
    maintaining temporal causality essential for time series forecasting.
    """
    
    def __init__(
        self,
        in_channels: int,
        out_channels: int,
        kernel_size: int,
        dilation: int = 1
    ):
        super(CausalConv1d, self).__init__()
        
        self.padding = (kernel_size - 1) * dilation
        self.conv = nn.Conv1d(
            in_channels,
            out_channels,
            kernel_size,
            padding=self.padding,
            dilation=dilation
        )
        
    def forward(self, x: torch.Tensor) -> torch.Tensor:
        # Apply convolution and remove the padding to maintain causality
        return self.conv(x)[:, :, :-self.padding] if self.padding > 0 else self.conv(x)


class FeatureExtractor:
    """
    Extracts and engineers features from raw hospital data for prediction models.
    
    This class handles the transformation of raw timestamp and patient data into
    meaningful features that improve prediction accuracy.
    
    Features extracted:
        - Temporal features: hour, day, month, day of week, holidays
        - Cyclical features: sin/cos encoding of time
        - Lag features: previous values for autoregressive modeling
        - Rolling statistics: moving averages, standard deviations
        - External factors: weather, events, seasonal patterns
    """
    
    def __init__(self, config: Optional[Dict[str, Any]] = None):
        self.config = config or {}
        self.scaler = StandardScaler()
        self.label_encoders = {}
        self.is_fitted = False
        
    def extract_temporal_features(self, timestamps: List[datetime]) -> pd.DataFrame:
        """
        Extract temporal features from datetime objects.
        
        Args:
            timestamps: List of datetime objects
            
        Returns:
            DataFrame with temporal features
        """
        df = pd.DataFrame({'timestamp': timestamps})
        
        # Basic temporal features
        df['hour'] = df['timestamp'].dt.hour
        df['day'] = df['timestamp'].dt.day
        df['month'] = df['timestamp'].dt.month
        df['year'] = df['timestamp'].dt.year
        df['day_of_week'] = df['timestamp'].dt.dayofweek
        df['day_of_year'] = df['timestamp'].dt.dayofyear
        df['week_of_year'] = df['timestamp'].dt.isocalendar().week.astype(int)
        df['quarter'] = df['timestamp'].dt.quarter
        df['is_weekend'] = (df['day_of_week'] >= 5).astype(int)
        
        # Cyclical encoding for periodic features
        # This preserves the circular nature of time (e.g., hour 23 is close to hour 0)
        df['hour_sin'] = np.sin(2 * np.pi * df['hour'] / 24)
        df['hour_cos'] = np.cos(2 * np.pi * df['hour'] / 24)
        df['month_sin'] = np.sin(2 * np.pi * df['month'] / 12)
        df['month_cos'] = np.cos(2 * np.pi * df['month'] / 12)
        df['day_of_week_sin'] = np.sin(2 * np.pi * df['day_of_week'] / 7)
        df['day_of_week_cos'] = np.cos(2 * np.pi * df['day_of_week'] / 7)
        
        # US Federal Holidays (simplified - in production use holiday library)
        holidays = self._get_holiday_dates()
        df['is_holiday'] = df['timestamp'].dt.date.isin(holidays).astype(int)
        
        # Time since midnight in hours (continuous)
        df['time_decimal'] = df['hour'] + df['timestamp'].dt.minute / 60
        
        return df
    
