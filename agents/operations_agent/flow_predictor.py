class EnsemblePredictor:
    """
    Ensemble predictor combining multiple models for robust forecasting.
    
    This class implements ensemble methods (bagging, boosting, stacking) to
    combine predictions from multiple models, reducing variance and improving
    accuracy through collective intelligence.
    
    Methods:
        - Bagging: Averaging predictions from multiple models
        - Weighted ensemble: Weighted average based on model performance
        - Stacking: Meta-model learning from base model predictions
    """
    
    def __init__(self, config: Optional[Dict[str, Any]] = None):
        self.config = config or {}
        self.models = []
        self.weights = []
        self.meta_model = None
        self.is_fitted = False
        
    def add_model(
        self,
        model: Any,
        weight: float = 1.0,
        model_type: str = "base"
    ):
        """
        Add a model to the ensemble.
        
        Args:
            model: The model instance
            weight: Weight for weighted ensemble
            model_type: 'base' for base models or 'meta' for stacking meta-model
        """
        if model_type == "meta":
            self.meta_model = model
        else:
            self.models.append(model)
            self.weights.append(weight)
    
    def fit(
        self,
        X_train: np.ndarray,
        y_train: np.ndarray,
        X_val: Optional[np.ndarray] = None,
        y_val: Optional[np.ndarray] = None
    ):
        """
        Fit all models in the ensemble.
        
        For stacking, uses validation predictions to train meta-model.
        """
        # Fit base models
        for model in self.models:
            model.fit(X_train, y_train)
        
        # Prepare stacking if meta-model exists
        if self.meta_model is not None and X_val is not None and y_val is not None:
            # Generate base model predictions on validation set
            val_predictions = []
            for model in self.models:
                pred = model.predict(X_val)
                val_predictions.append(pred)
            
            # Stack predictions as features for meta-model
            X_meta = np.column_stack(val_predictions)
            
            # Fit meta-model
            self.meta_model.fit(X_meta, y_val)
        
        self.is_fitted = True
    
    def predict(
        self,
        X: np.ndarray,
        method: str = "weighted"
    ) -> np.ndarray:
        """
        Generate ensemble predictions.
        
        Args:
            X: Input features
            method: 'bagging', 'weighted', or 'stacking'
            
        Returns:
            Ensemble predictions
        """
        if not self.is_fitted:
            raise ValueError("Model must be fitted before prediction")
        
        if method == "bagging":
            # Simple average of all models
            predictions = np.array([model.predict(X) for model in self.models])
            return np.mean(predictions, axis=0)
        
        elif method == "weighted":
            # Weighted average based on model weights
            predictions = np.array([model.predict(X) for model in self.models])
            weights = np.array(self.weights) / sum(self.weights)
            return np.sum(predictions * weights.reshape(-1, 1), axis=0)
        
        elif method == "stacking" and self.meta_model is not None:
            # Stacking: use meta-model on base model predictions
            base_predictions = []
            for model in self.models:
                pred = model.predict(X)
                base_predictions.append(pred)
            
            X_meta = np.column_stack(base_predictions)
            return self.meta_model.predict(X_meta)
        
        else:
            raise ValueError(f"Unknown ensemble method: {method}")


