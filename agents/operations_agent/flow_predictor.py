    def create_lag_features(
        self,
        series: pd.Series,
        lags: List[int] = [1, 2, 3, 6, 12, 24, 48]
    ) -> pd.DataFrame:
        """
        Create lag features for autoregressive modeling.
        
        Args:
            series: Time series data
            lags: List of lag periods (in hours for hourly data)
            
        Returns:
            DataFrame with lag features
        """
        df = pd.DataFrame(index=series.index)
        
        for lag in lags:
            df[f'lag_{lag}'] = series.shift(lag)
        
        return df
    
    def create_rolling_features(
        self,
        series: pd.Series,
        windows: List[int] = [6, 12, 24, 168]  # 6h, 12h, 24h, 1 week
    ) -> pd.DataFrame:
        """
        Create rolling statistics features.
        
        Args:
            series: Time series data
            windows: List of window sizes (in hours)
            
        Returns:
            DataFrame with rolling features
        """
        df = pd.DataFrame(index=series.index)
        
        for window in windows:
            rolling = series.rolling(window=window, min_periods=1)
            df[f'rolling_mean_{window}'] = rolling.mean()
            df[f'rolling_std_{window}'] = rolling.std()
            df[f'rolling_min_{window}'] = rolling.min()
            df[f'rolling_max_{window}'] = rolling.max()
            df[f'rolling_skew_{window}'] = rolling.skew()
        
        return df
    
